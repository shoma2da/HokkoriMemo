package com.example.withpartner.data;

public class Hokkori {
    private String str;
    private long time;
    
    public Hokkori(String str, long time) {
        this.str = str;
        this.time = time;
    }
    
    public String getStr() {
        return this.str;
    }
    
    public long getTime() {
        return time;
    }
}
